                                    <hr>

                                    <p class="Texto">A formulação da estratégia como um processo reativo. O ambiente é um conjunto de forças gerais, responsável pela geração de estratégias. As organizações necessitam se adequar a estas forças, ou elas morrerão.
                                    <br><br>Como principal crítica a esta escola destaca-se o fato de as dimensões do ambiente serem muito abstratas.</p>



                                    <br><p class="Texto"><strong><i class="fa fa-arrow-right CorLaranja"></i> Escola de Configuração  </strong></p>
                                    <hr>

                                    <p class="Texto">A formulação da estratégia como um processo de transformação. As organizações podem ser escritas em termos de algum tipo de configuração estável em um período de tempo, que dá origem às estratégias. Os períodos de estabilidade são interrompidos, dando origem a novas configurações. Os ciclos de interrupções e mudanças são estáveis, e o sucesso está na administração destes ciclos. As estratégias necessitam ser adequadas ao contexto.
                                    <br><br>As principais críticas relatam a dificuldade de classificação das organizações em configurações preestabelecidas, já que existem várias nuances. Outra crítica refere-se ao fato de as organizações estarem constantemente mudando, não apenas quando os períodos de estabilidade são interrompidos. A abordagem desta escola pode simplificar uma realidade complexa.</p>
                                    

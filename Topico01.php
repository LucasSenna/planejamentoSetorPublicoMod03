            <p class="Texto">
                Os autores classificam as dez escolas em três agrupamentos. As três primeiras são de natureza prescritiva – mais preocupadas em saber como as estratégias devem ser formuladas do que em como elas são formuladas. A primeira delas, a Escola do Design, que nos anos 80 apresentou a estrutura básica sobre a qual as duas outras foram construídas, focaliza a formulação de estratégias como um processo de desenho informal. A segunda escola – a Escola do Planejamento – desenvolvida paralelamente nos anos 60 e que teve seu auge nos anos 70, formalizou essa perspectiva, vendo a formulação de estratégias como um processo de planejamento formal separado e sistemático.
                <br><br>Essa escola perdeu espaço nos anos 80 para a Escola do Posicionamento, menos preocupada com o processo de formulação de estratégias e mais com o conteúdo real das mesmas, concentrando-se na seleção de posições estratégicas no mercado. 
                <br><br>As seis escolas seguintes consideram aspectos específicos do processo de formulação das estratégias e se preocupam menos com a descrição do comportamento estratégico ideal do que com a descrição de como as estratégias são formuladas de fato.
                <br><br>Alguns autores proeminentes associam, há muito, a estratégia com espírito empreendedor e descrevem o processo em termos de criação da visão pelo grande líder. Se a estratégia pode ser uma visão pessoal, então sua formulação também precisa ser entendida como o processo de obtenção do conceito na mente de um indivíduo. Analogamente, foi também desenvolvida uma escola cognitiva, a qual busca usar as mensagens da Psicologia Cognitiva para entrar na mente do estrategista.
                <br><br>Cada uma das quatro escolas seguintes procurou abrir o processo de formulação de estratégia além do indivíduo. Para a Escola de Aprendizado, o mundo é complexo demais para que as estratégias sejam desenvolvidas, de uma só vez, como planos ou visões claras. Portanto, a estratégia deve surgir em passos curtos, à medida que a organização se adapta, ou aprende. A Escola do Poder trata a formulação da estratégia como um processo de negociação, seja por grupos conflitantes, dentro de uma organização, ou pelas próprias organizações, enquanto confrontam seus ambientes externos. Em comparação com esta, há uma outra escola de pensamento, a escola cultural que considera a formulação de estratégia como enraizada na cultura da organização, vendo o processo como sendo fundamentalmente coletivo e cooperativo. E há os proponentes de uma Escola Ambiental em que a formulação das estratégias é um processo no qual a iniciativa não está dentro da organização, mas em seu contexto externo. Assim sendo,  procuram compreender as pressões impostas sobre as organizações.
                <br><br>O grupo final compreende apenas uma escola – a de Configuração. As pessoas dessa escola, em busca da integração, agrupam vários elementos: processo de formulação de estratégias, conteúdo destas e estruturas organizacionais e seus contextos. Isso em estágios, às vezes, sequenciados ao longo do tempo, para descrever os ciclos de vida das organizações. No entanto, se as organizações se acomodam em quadros estáveis, então a formulação de estratégias deve descrever o salto de um quadro para o outro. E assim, um outro lado desta escola descreve o processo como sendo de transformação, incorporando grande parte da literatura e da prática prescritiva sobre mudanças estratégicas.</p>
